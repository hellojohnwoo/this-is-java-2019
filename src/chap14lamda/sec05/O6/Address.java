package chap14lamda.sec05.O6;

public class Address {
    private String coutry;
    private String city;

    public Address(String coutry, String city) {
        this.coutry = coutry;
        this.city = city;
    }

    public String getCoutry() {
        return coutry;
    }

    public String getCity() {
        return city;
    }
}
