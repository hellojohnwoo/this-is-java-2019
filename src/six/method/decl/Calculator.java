package six.method.decl;

public class Calculator {

    // Method
    void powerOn() {
        System.out.println("Turn On Light");
    }

    int plus(int x, int y) {
        int result = x + y;
        return result;
    }

    double divide(int x, int y) {
        double result = (double)x / (double)y;
        return result;
    }

    void powerOff() {
        System.out.println("Turn Off Light");
    }


}
